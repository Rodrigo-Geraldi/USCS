#include <stdio.h>

int main()
{

printf("quadrado de 10 asteriscos\n");

for(int l=0; l < 10; l++){
    for(int c = 0; c < 10; c++){
        printf("*");
    }
    printf("\n");
}

printf("\nquadrado de 9 asteriscos \n");

for(int l=0; l < 9; l++){
    for(int c = 0; c < 9; c++){
        printf("*");
    }
    printf("\n");
}

printf("\nquadrado de 8 asteriscos \n");

for(int l=0; l < 8; l++){
    for(int c = 0; c < 8; c++){
        printf("*");
    }
    printf("\n");
}


}

